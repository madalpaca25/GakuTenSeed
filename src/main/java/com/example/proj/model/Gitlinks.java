package com.example.proj.model;

public class Gitlinks {

    private int gitID;
    private int myGitLinkID;
    
    private int myStudentID;
    private String myGitLink;
    private String myDesc;

    public Gitlinks() {}

    public int getGitID() {
        return gitID;
    }

    public void setGitID(int gitID) {
        this.gitID = gitID;
    }

    public int getMyGitLinkID() {
        return myGitLinkID;
    }

    public void setMyGitLinkID(int myGitLinkID) {
        this.myGitLinkID = myGitLinkID;
    }

    public int getMyStudentID() {
        return myStudentID;
    }

    public void setMyStudentID(int myStudentID) {
        this.myStudentID = myStudentID;
    }

    public String getMyGitLink() {
        return myGitLink;
    }

    public void setMyGitLink(String myGitLink) {
        this.myGitLink = myGitLink;
    }

    public String getMyDesc() {
        return myDesc;
    }

    public void setMyDesc(String myDesc) {
        this.myDesc = myDesc;
    }

   
}
